// Language Manager
class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('agb-language') || 'pt';
        this.init();
    }

    init() {
        // Apply saved language on load
        this.applyLanguage(this.currentLang);

        // Set up language toggle buttons
        this.setupLanguageToggles();

        // Update active language indicator
        this.updateActiveLanguage();
    }

    setupLanguageToggles() {
        const ptButtons = document.querySelectorAll('[data-lang-btn="pt"]');
        const enButtons = document.querySelectorAll('[data-lang-btn="en"]');

        ptButtons.forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                this.switchLanguage('pt');
            });
        });

        enButtons.forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                this.switchLanguage('en');
            });
        });
    }

    switchLanguage(lang) {
        if (lang !== this.currentLang) {
            this.currentLang = lang;
            localStorage.setItem('agb-language', lang);
            this.applyLanguage(lang);
            this.updateActiveLanguage();
        }
    }

    applyLanguage(lang) {
        const elements = document.querySelectorAll('[data-i18n]');

        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = translations[lang][key];

            if (translation !== undefined) {
                // Handle different element types
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    if (element.placeholder !== undefined) {
                        element.placeholder = translation;
                    }
                } else {
                    element.textContent = translation;
                }
            }
        });
    }

    updateActiveLanguage() {
        // Update PT button
        const ptButtons = document.querySelectorAll('[data-lang-btn="pt"]');
        const enButtons = document.querySelectorAll('[data-lang-btn="en"]');

        ptButtons.forEach(btn => {
            if (this.currentLang === 'pt') {
                btn.classList.add('text-white');
                btn.classList.remove('text-zinc-400');
            } else {
                btn.classList.remove('text-white');
                btn.classList.add('text-zinc-400');
            }
        });

        enButtons.forEach(btn => {
            if (this.currentLang === 'en') {
                btn.classList.add('text-white');
                btn.classList.remove('text-zinc-400');
            } else {
                btn.classList.remove('text-white');
                btn.classList.add('text-zinc-400');
            }
        });
    }
}

// Initialize language manager when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        window.langManager = new LanguageManager();
    });
} else {
    window.langManager = new LanguageManager();
}
